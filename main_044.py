if __name__ == "__main__":
    estacao = int(input("Digite um número para a estação do ano (1 a 4): "))
    if estacao == 1:
        print("Verão.")
    elif estacao == 2:
        print("Outono.")
    elif estacao == 3:
        print("Inverno.")
    elif estacao == 4:
        print("Primavera")
    else:
        print("Digíto um número válido!")
    exit()