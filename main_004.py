def conversao_de_tipos():
    altura = 1.72
    altura_int = int(altura)
    numero = 10
    numero_float = float(numero)

    print("altura (float): ", altura)
    print("altura (int): ", altura_int)
    print("numero (int): ", numero)
    print("numero (float): ", numero_float)

if __name__ == "__main__":
    conversao_de_tipos()
    exit()