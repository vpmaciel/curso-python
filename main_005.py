def operacoes_aritmeticas():
    preco = 4
    quantidade = 3
    print("Soma:", preco + quantidade)
    print("Divisao (inteira):", preco // quantidade)
    print("Divisao:", preco / quantidade)
    print("Subtração:", preco - quantidade)
    print("Multiplicação:", preco * quantidade)
    print("Módulo: ", preco % 2)

if __name__ == "__main__":
    operacoes_aritmeticas()
    exit()