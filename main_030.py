if __name__ == "__main__":
    valor1 = int(input("Digite o primeiro número inteiro: "))
    valor2 = int(input("Digite o segundo número inteiro: "))
    # Operações com operadores compostos
    valor1 += valor2
    print(f"Apos valor1 += valor2: {valor1}")
    valor1 -= valor2
    print(f"Apos valor1 -= valor2: {valor1}")
    valor1 *= valor2
    print(f"Apos valor1 *= valor2: {valor1}")
    valor1 /= valor2
    print(f"Apos valor1 /= valor2: {valor1}")
    valor1 %= valor2
    print(f"Apos valor1 %= valor2: {valor1}")

    exit()